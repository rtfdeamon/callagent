url = "http://127.0.0.1:8000/tts"
payload = {"text": text}

print(f"Отправка запроса на синтез страницы текста ({len(text)} символов)...")
try:
    response = requests.post(url, json=payload, timeout=300)
    if response.status_code == 200:
        output_file = "/home/dmitriy/work/callagent/ai_telemarketer/test_page_andreev.wav"
        with open(output_file, "wb") as f:
            f.write(response.content)
        print(f"Успешно! Аудио сохранено в: {output_file}")
        print(f"Размер файла: {len(response.content) / 1024 / 1024:.2f} МБ")
    else:
        print(f"Ошибка: {response.status_code}")
        print(response.text)
except Exception as e:
    print(f"Исключение: {e}")
